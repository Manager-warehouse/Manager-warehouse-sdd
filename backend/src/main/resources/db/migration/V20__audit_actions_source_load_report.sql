    'OUTBOUND_QC_FAIL_QUARANTINE', 'DELIVERY_ORDER_QC_APPROVE', 'DELIVERY_ORDER_WAREHOUSE_APPROVE',
    'DELIVERY_ORDER_WAREHOUSE_REJECT', 'INVOICE_AUTO_CREATE', 'RECEIPT_RETURN_CONFIRM', 'RECEIPT_REJECT',
    'RECEIPT_APPROVE', 'QUARANTINE_RTV_CREATE', 'QUARANTINE_RTV_CONFIRM', 'RECEIPT_QC_CONFIRM',
    'RECEIPT_QC_SUBMIT', 'RECEIPT_PUTAWAY_COMPLETE', 'INVENTORY_UPDATE', 'TRANSFER_APPROVE', 'TRANSFER_DEPART',
